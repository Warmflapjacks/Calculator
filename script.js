function addNumbers (variableOne, variableTwo) {
    return variableOne + variableTwo;
}

function subtractNumbers (variableOne, variableTwo) {
    return variableOne - variableTwo;
}

function multiplyNumbers (variableOne, variableTwo) {
    return variableOne * variableTwo;
}

function divideNumbers (variableOne, variableTwo) {
    return variableOne / variableTwo;
}
